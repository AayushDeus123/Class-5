#Greater or Smaller
num1 = int(input('Enter your First Number : '))
num2 = int(input('Enter your Second Number : '))
if num1>num2:
    print('\nThe number ',num1,' is greater than ',num2)
elif num1<num2:
    print('\nThe number ',num1,' is smaller than ',num2)
else:
    print('\nThey both are equal')